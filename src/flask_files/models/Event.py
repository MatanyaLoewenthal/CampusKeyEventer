class Event:
    def __init__(self, name, year, month, startDay, endDay, timeStartDay, timeEndDay, location):
        self.name = name
        self.year = year
        self.month = month
        self.startDay = startDay
        self.endDay = endDay
        self.timeStartDay = timeStartDay
        self.timeEndDay = timeEndDay
        self.location = location

    
    
